// ---------- Classe HID Wheel ------------------------------------------------
extern void FfbOnUsbData(uint8_t *data, uint16_t len);  // implementado em ffb.cpp

class WheelFFBDevice : public USBHIDDevice {
public:
  WheelFFBDevice() {}

  // Chamado pelo TinyUSB para obter o descriptor HID
  uint16_t _onGetDescriptor(uint8_t *dst) override {
    memcpy(dst, _hidDescriptor, sizeof(_hidDescriptor));
    return sizeof(_hidDescriptor);
  }

  // Chamado pelo TinyUSB quando o PC envia um SetReport (FFB output + config reports)
  void _onSetReport(uint8_t report_id, const uint8_t *data, uint16_t len) override {
    // Monta buffer com report_id no byte 0, igual ao formato que FfbOnUsbData espera
    static uint8_t buf[65];
    buf[0] = report_id;
    if (len > 64) len = 64;
    memcpy(buf + 1, data, len);
    FfbOnUsbData(buf, len + 1);
  }

  // Envia report de input (joystick axes + buttons)
  bool SendJoystickReport(uint16_t x, uint16_t y, uint16_t z,
                          uint16_t rx, uint16_t ry, uint32_t buttons) {
    uint8_t j[12];
    j[0]  = x  & 0xFF;
    j[1]  = x  >> 8;
    j[2]  = y  & 0xFF;
    j[3]  = y  >> 8;
    // Z(12) + RX(4 lsb)
    j[4]  = z  & 0xFF;
    j[5]  = ((z >> 8) & 0x0F) | ((rx & 0x0F) << 4);
    j[6]  = (rx >> 4) & 0xFF;
    // RY(12) + buttons(4 lsb)
    j[7]  = ry & 0xFF;
    j[8]  = ((ry >> 8) & 0x0F) | ((buttons & 0x0F) << 4);
    j[9]  = (buttons >> 4)  & 0xFF;
    j[10] = (buttons >> 12) & 0xFF;
    j[11] = (buttons >> 20) & 0xFF;
    return _hid->SendReport(REPORT_ID_JOYSTICK, j, 12);
  }

  // Envia PID State Report (opcional, exigido por alguns jogos)
  bool SendPIDState(uint8_t flags, bool effectPlaying, uint8_t effectBlockIndex) {
    uint8_t buf[2];
    buf[0] = (flags & 0x1F);          // 5 bits de flags
    buf[1] = (effectPlaying ? 1 : 0) | ((effectBlockIndex & 0x7F) << 1);
    return _hid->SendReport(REPORT_ID_PID_STATE, buf, 2);
  }

  // Envia Config Report F2 (resposta ao Wheel Control)
  bool SendConfigReport(const uint8_t *data, uint8_t len) {
    uint8_t buf[63] = {0};
    if (len > 63) len = 63;
    memcpy(buf, data, len);
    return _hid->SendReport(REPORT_ID_CONFIG_OUT, buf, 63);
  }

  void begin(USBHID &hid) {
    _hid = &hid;
    hid.addDevice(this, sizeof(_hidDescriptor));
  }

private:
  USBHID *_hid = nullptr;
};

